        return [
            'password' =>  'required|min:8|max:20',
            'email' =>     'required|regex:/^([a-z0-9\+_\-]+)(\.[a-z0-9\+_\-]+)*@([a-z0-9\-]+\.)+[a-z]{2,6}$/ix',
            'login' =>     'required|min:4|max:20|regex:/^(?!.*\.\.)(?!.*\.$)[^\W][\w.]{0,29}$/',
